#!/usr/bin/env node
import { readFile, stat, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { dataScriptsFromIndex } from './script-sources.mjs';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const rootDir = path.resolve(__dirname, '..');
const outputPath = 'data/dataset-file-metadata.js';

function parseArgs(argv) {
  const allowed = new Set(['--check']);
  const unknown = argv.slice(2).filter((arg) => !allowed.has(arg));
  if (unknown.length) throw new Error(`Unknown argument(s): ${unknown.join(' ')}`);
  return { check: argv.includes('--check') };
}

function projectPath(relativePath) {
  return path.join(rootDir, relativePath);
}

function datasetKeyForScript(script) {
  return path.basename(script, '.js');
}

async function readProjectFile(relativePath) {
  return readFile(projectPath(relativePath), 'utf8');
}

async function datasetEntries() {
  const scripts = dataScriptsFromIndex(await readProjectFile('index.html'));
  const entries = [];
  for (const script of scripts) {
    const info = await stat(projectPath(script));
    const mtimeMs = Math.round(info.mtimeMs);
    entries.push([
      datasetKeyForScript(script),
      {
        path: script,
        mtimeMs,
        mtime: new Date(mtimeMs).toISOString(),
      },
    ]);
  }
  return entries;
}

function renderSource(entries) {
  const maxMtimeMs = entries.reduce((max, [, entry]) => Math.max(max, entry.mtimeMs || 0), 0);
  const metadata = {
    generatedAt: maxMtimeMs ? new Date(maxMtimeMs).toISOString() : '',
    source: 'dataset file modification times from index.html registrations',
    files: Object.fromEntries(entries),
  };
  const body = JSON.stringify(metadata, null, 2);
  return `/*\n * Generated by scripts/update-dataset-file-metadata.mjs.\n * Do not edit by hand.\n */\n(function () {\n  window.DATASET_FILE_METADATA = ${body};\n})();\n`;
}

async function main() {
  const { check } = parseArgs(process.argv);
  const source = renderSource(await datasetEntries());
  const target = projectPath(outputPath);
  if (check) {
    let existing = '';
    try {
      existing = await readFile(target, 'utf8');
    } catch (error) {
      if (error.code !== 'ENOENT') throw error;
    }
    if (existing !== source) {
      throw new Error('Dataset file metadata is stale. Run pnpm update:dataset-file-metadata.');
    }
    console.log(`${outputPath} is current`);
    return;
  }
  await writeFile(target, source);
  console.log(`wrote ${outputPath}`);
}

main().catch((err) => {
  console.error(err.stack || err.message);
  process.exit(1);
});
